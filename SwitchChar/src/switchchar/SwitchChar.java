package switchchar;


public class SwitchChar {


    public static void main(String[] args) {
        char ch='t';
        switch(ch){
            case 'a':
            System.out.println("a");
            break;
            case 'b':
                System.out.println("b");
                break;
            case 't':
                System.out.println("T");
                break;
            default:
                System.out.println("default");
        }
    }
    
}
