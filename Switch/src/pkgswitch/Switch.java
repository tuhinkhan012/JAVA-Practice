package pkgswitch;


public class Switch {


    public static void main(String[] args) {
       int i=2;
       switch(i){
           case 1:
               System.out.println("case1");
               break;
           case 2:
               System.out.println("case2");
                   break;
               case 3:
                   System.out.println("case3");
                   break;
               default:
                   System.out.println("default");
       }
    }
    
}
