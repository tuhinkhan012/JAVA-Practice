package switchnum;

public class SwitchNum {

    public static void main(String[] args) {
        int num=2;
        switch(num){
            case 1:
                System.out.println(num+1);
                break;
                case 2:
                System.out.println(num+2);
                break;
                case 3:
                System.out.println(num+3);
                break;
                default:
                System.out.println(num+4);
        
        }
       
    }
    
}
